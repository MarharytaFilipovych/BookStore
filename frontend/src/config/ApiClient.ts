import axios, { AxiosInstance, AxiosRequestConfig, AxiosResponse } from 'axios';
import {API_ENDPOINTS, TokenResponseDTO, RefreshTokenDTO, Role} from '../types';

class ApiClient {
    private readonly client: AxiosInstance;
    private isRefreshing = false; // ‚úÖ ADDED: Prevent multiple refresh attempts
    private failedQueue: Array<{
        resolve: (value: any) => void;
        reject: (error: any) => void;
        config: any;
    }> = []; // ‚úÖ ADDED: Queue for failed requests during refresh

    constructor() {
        console.log('üèóÔ∏è Initializing ApiClient...');

        this.client = axios.create({
            baseURL: process.env.REACT_APP_API_URL || 'http://localhost:8084',
            timeout: 15000,
            headers: {
                'Content-Type': 'application/json',
            },
        });

        console.log(`üåê ApiClient configured with baseURL: ${this.client.defaults.baseURL}`);
        this.setupInterceptors();
    }

    // ‚úÖ ADDED: Process queued requests after successful refresh
    private processQueue(error: any, token: string | null = null) {
        this.failedQueue.forEach(({ resolve, reject, config }) => {
            if (error) {
                reject(error);
            } else {
                if (token) {
                    config.headers.Authorization = `Bearer ${token}`;
                }
                resolve(this.client(config));
            }
        });

        this.failedQueue = [];
    }

    private setupInterceptors() {
        console.log('‚öôÔ∏è Setting up request/response interceptors...');

        this.client.interceptors.request.use(
            (config) => {
                console.log(`üöÄ Making ${config.method?.toUpperCase()} request to: ${config.baseURL}${config.url}`);

                // Only set token if Authorization header isn't already present
                if (!config.headers.Authorization) {
                    const token = localStorage.getItem('accessToken');
                    if (token) {
                        config.headers.Authorization = `Bearer ${token}`;
                        console.log('üîë JWT token attached from localStorage');
                    } else {
                        console.log('‚ö†Ô∏è No JWT token found in localStorage');
                    }
                } else {
                    console.log('üîë Using existing Authorization header');
                }

                if (config.data) {
                    console.log('üì¶ Request data:', config.data);
                }

                if (config.params) {
                    console.log('üîç Request params:', config.params);
                }

                return config;
            },
            (error) => {
                console.error('‚ùå Request interceptor error:', error);
                return Promise.reject(error);
            }
        );

        this.client.interceptors.response.use(
            (response) => {
                console.log(`‚úÖ API response from ${response.config.url}:`, {
                    status: response.status,
                    statusText: response.statusText,
                    dataSize: JSON.stringify(response.data).length + ' bytes'
                });

                return response;
            },
            async (error) => {
                const originalRequest = error.config;

                console.error(`‚ùå API error from ${originalRequest?.url}:`, {
                    status: error.response?.status,
                    statusText: error.response?.statusText,
                    message: error.response?.data?.message || error.message
                });

                // Handle 401 errors (token expired)
                if (error.response?.status === 401 && !originalRequest._retry) {
                    console.log('üîÑ Token expired (401), attempting refresh...');

                    // ‚úÖ FIXED: Handle concurrent requests during refresh
                    if (this.isRefreshing) {
                        console.log('‚è≥ Refresh already in progress, queuing request...');
                        return new Promise((resolve, reject) => {
                            this.failedQueue.push({ resolve, reject, config: originalRequest });
                        });
                    }

                    originalRequest._retry = true;
                    this.isRefreshing = true;

                    try {
                        const refreshToken = localStorage.getItem('refreshToken');
                        const savedUser = localStorage.getItem('user');
                        const savedRole = localStorage.getItem('role');

                        if (!refreshToken || !savedUser || !savedRole) {
                            console.log('‚ùå Missing refresh data, redirecting to login');
                            throw new Error('Missing authentication data');
                        }

                        console.log('üîÑ Calling refresh token endpoint...');

                        // ‚úÖ FIXED: Parse user data properly
                        let email: string;
                        try {
                            const userData = JSON.parse(savedUser);
                            email = userData.email;
                        } catch (parseError) {
                            console.error('‚ùå Failed to parse user data:', parseError);
                            throw new Error('Invalid user data');
                        }

                        // ‚úÖ FIXED: Use correct refresh token payload structure
                        const refreshData: RefreshTokenDTO = {
                            refresh_token: refreshToken, // ‚úÖ FIXED: Correct field name
                            email: email,
                            role: savedRole as Role
                        };

                        console.log('üì§ Sending refresh token request:', {
                            endpoint: API_ENDPOINTS.auth.refresh,
                            email,
                            role: savedRole,
                            hasRefreshToken: !!refreshToken
                        });

                        // ‚úÖ FIXED: Use a fresh axios instance to avoid interceptor loop
                        const response = await axios.post<TokenResponseDTO>(
                            `${this.client.defaults.baseURL}${API_ENDPOINTS.auth.refresh}`,
                            refreshData,
                            {
                                headers: {
                                    'Content-Type': 'application/json'
                                }
                            }
                        );

                        const newAccessToken = response.data.access_token;

                        // Store new tokens
                        localStorage.setItem('accessToken', newAccessToken);
                        if (response.data.refresh_token) {
                            localStorage.setItem('refreshToken', response.data.refresh_token);
                            console.log('üîÑ Both access and refresh tokens updated');
                        } else {
                            console.log('üîÑ Only access token updated');
                        }

                        console.log('‚úÖ Token refresh successful, processing queued requests...');

                        // ‚úÖ FIXED: Update original request and process queue
                        originalRequest.headers.Authorization = `Bearer ${newAccessToken}`;
                        this.processQueue(null, newAccessToken);

                        return this.client(originalRequest);

                    } catch (refreshError) {
                        console.error('‚ùå Token refresh failed:', refreshError);

                        this.clearAuthData();
                        this.processQueue(refreshError, null);

                        console.log('üö™ Redirecting to welcome page...');
                        window.location.href = '/';

                        return Promise.reject(refreshError);
                    } finally {
                        this.isRefreshing = false; // ‚úÖ FIXED: Reset refresh flag
                    }
                }

                return Promise.reject(error);
            }
        );

        console.log('‚úÖ Interceptors configured successfully');
    }

    private clearAuthData() {
        console.log('üßπ Clearing authentication data...');

        const keysToRemove = [
            'accessToken',
            'refreshToken',
            'user',
            'role',
            'basket'
        ];

        keysToRemove.forEach(key => {
            if (localStorage.getItem(key)) {
                localStorage.removeItem(key);
                console.log(`üóëÔ∏è Removed ${key} from localStorage`);
            }
        });

        console.log('‚úÖ Authentication data cleared');
    }

    async get<T = any>(url: string, config?: AxiosRequestConfig): Promise<AxiosResponse<T>> {
        console.log(`üìñ GET request initiated: ${url}`);

        if (config?.params) {
            console.log('üîç GET params:', config.params);
        }

        const response = await this.client.get<T>(url, config);

        console.log(`üìñ GET request completed: ${url} (${response.status})`);
        return response;
    }

    async post<T = any>(url: string, data?: any, config?: AxiosRequestConfig): Promise<AxiosResponse<T>> {
        console.log(`üìù POST request initiated: ${url}`);

        if (data) {
            console.log('üì¶ POST data:', typeof data === 'object' ? JSON.stringify(data, null, 2) : data);
        }

        if (config?.headers) {
            console.log('üìã Custom headers:', config.headers);
        }

        const response = await this.client.post<T>(url, data, config);

        console.log(`üìù POST request completed: ${url} (${response.status})`);
        return response;
    }

    async put<T = any>(url: string, data?: any, config?: AxiosRequestConfig): Promise<AxiosResponse<T>> {
        console.log(`‚úèÔ∏è PUT request initiated: ${url}`);

        if (data) {
            console.log('üì¶ PUT data:', typeof data === 'object' ? JSON.stringify(data, null, 2) : data);
        }

        if (config?.params) {
            console.log('üîç PUT params:', config.params);
        }

        if (config?.headers) {
            console.log('üìã Custom headers:', config.headers);
        }

        const response = await this.client.put<T>(url, data, config);

        console.log(`‚úèÔ∏è PUT request completed: ${url} (${response.status})`);
        return response;
    }

    async delete<T = any>(url: string, config?: AxiosRequestConfig): Promise<AxiosResponse<T>> {
        console.log(`üóëÔ∏è DELETE request initiated: ${url}`);

        if (config?.params) {
            console.log('üîç DELETE params:', config.params);
        }

        const response = await this.client.delete<T>(url, config);

        console.log(`üóëÔ∏è DELETE request completed: ${url} (${response.status})`);
        return response;
    }

    getBaseURL(): string {
        return this.client.defaults.baseURL || '';
    }

    setBaseURL(baseURL: string): void {
        console.log(`üåê Updating base URL from ${this.client.defaults.baseURL} to ${baseURL}`);
        this.client.defaults.baseURL = baseURL;
    }

    setDefaultHeader(key: string, value: string): void {
        console.log(`üìã Setting default header: ${key} = ${value}`);
        this.client.defaults.headers.common[key] = value;
    }

    debugAuthState(): void {
        console.log('üîç Current authentication state:', {
            hasAccessToken: !!localStorage.getItem('accessToken'),
            hasRefreshToken: !!localStorage.getItem('refreshToken'),
            hasUser: !!localStorage.getItem('user'),
            hasRole: !!localStorage.getItem('role'),
            baseURL: this.client.defaults.baseURL,
            isRefreshing: this.isRefreshing,
            queuedRequests: this.failedQueue.length
        });
    }
}

export const apiClient = new ApiClient();

// ‚úÖ ADDED: Debug helper
(window as any).debugApi = () => apiClient.debugAuthState();